.MODEL SMALL
.STACK 100H
.DATA

MSG1 DB "ENTER 5 NUMBERS (1 to 9): $"
MSG2 DB 10,13,"ENTER NUMBER YOU WANT SEARCH: $"
MSG3 DB 10,13,"NUMBER FOUND $" 
MSG4 DB 10,13,"NUMBER NOT FOUND.$"

ARR DB 5 DUP(?)
SIZE DB 5
     
.CODE
MAIN PROC
MOV AX, @DATA
MOV DS, AX

LEA DX, MSG1
MOV AH, 9
INT 21H

XOR CX, CX

MOV CL, SIZE
LEA SI, ARR  

INPUTLOOP:   
	
	MOV AH, 1
	INT 21H
	SUB AL, 48
	MOV [SI], AL
	
	INC SI
			
	MOV AH, 2
	MOV DL, " "
	INT 21H

LOOP INPUTLOOP

LEA DX, MSG2
MOV AH, 9
INT 21H

MOV AH, 1
INT 21H
SUB AL, 30H    
MOV BL, AL

XOR CX, CX

MOV CL, SIZE
MOV BH, SIZE
LEA SI, ARR

LINEARLOOP:    
	MOV AL, [SI]
	
	CMP AL, BL
	JE FOUND:
    
    INC SI
LOOP LINEARLOOP  

LEA DX, MSG4
MOV AH, 9
INT 21H  

JMP EXIT

FOUND:  

SUB BH, CL
INC BH 

LEA DX, MSG3
MOV AH, 9
INT 21H

EXIT:
MOV AH, 4CH
INT 21H

MAIN ENDP
END MAIN